    private void loadAllCustomer() throws ClassNotFoundException, SQLException {
        model = (DefaultTableModel) tbl.getModel();
        model.setRowCount(0);
        for (CustomerDTO c : new CustomerController().loadAllCustomer()) {
            model.addRow(new Object[]{
                c.getCustId(),
                c.getContact(),
                c.getName(),
                c.getAddrss()

            });
        }
    }

    private void clearText() {
        txtCustAddress.setText("");
        txtCustContact.setText("");
        txtCustName.setText("");
    }

    private void loadCustomerId() throws ClassNotFoundException, SQLException {
        String id = new CustomerController().getLastId();
        if (id == null) {
            lblId.setText("C001");
        } else {
            String split[] = id.split("C");
            int num = Integer.parseInt(split[1]);
            num++;

            if (num < 10) {
                lblId.setText("C00" + num);
            } else if (num < 100) {
                lblId.setText("C0" + num);
            } else {
                lblId.setText("C" + num);
            }

        }
    }
