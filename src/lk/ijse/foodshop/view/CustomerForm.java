    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            if (txtCustContact != null && txtCustName != null && txtCustAddress != null) {
                String id = lblId.getText();
                String contact = txtCustContact.getText();
                String custName = txtCustName.getText();
                String custAddress = txtCustAddress.getText();

                String checkContact = new CustomerController().checkContact(contact);
                if (checkContact != null) {
                    JOptionPane.showMessageDialog(this, "this phone number is allready exists");
                } else {
                    boolean isAdd = new CustomerController().isAddedCustomer(new CustomerDTO(id, contact, custName, custAddress));

                    if (isAdd) {
                        JOptionPane.showMessageDialog(this, "Added customer");
                        loadAllCustomer();
                        clearText();
                        loadCustomerId();
                    } else {
                        JOptionPane.showMessageDialog(this, "check again");
                    }
                }
            }else{
                JOptionPane.showMessageDialog(this, "check again");
            }

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(CustomerForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddActionPerformed

